							<div class="col-md-12 mt-sm-20"><p>semacam itu. Sigale-gale kemudian digerakkan oleh seorang dalang yang menarik tali-tali yang dipasang pada Sigale-gale.
							Legenda Sigale-gale ini nyatanya menjadi inspirasi bagi dua anak muda Batak untuk membuat Sigale-gale kembali bergerak sendiri tanpa bantuan tali-tali yang ditarik manual oleh dalang. Adalah Crisman Wise Patuan Silaban dan Deny K. Sihombing, alumni SMA Negeri 2 Yayasan Soposurung Balige lulusan tahun 2010. Keduanya menamatkan studi dari Faklutas Teknik, Universitas Indonesia pada tahun 2014. Crisman Silaban mengambil jurusan Teknik Elektro dan selama kuliah bergabung dengan <b style="color: black;"> <i>Tim Robotika Universitas Indonesia (TRUI)</i></b> di divisi Robo Soccer Humanoid League atau robot pemain bola. Pada tahun kedua bergabung di TRUI, Crisman Silaban menjadi tim inti sekaligus penanggung jawab divisi elektrik. Tidak jauh berbeda, Denny Sihombing mengambil jurusan Metalurgi dan selama kuliah juga bergabung dengan TRUI dan menjadi Ketua Tim Robot Seni (robot penari). Masing-masing mereka telah membawa tim untuk memenangkan berbagai kontes robot dan menerima penghargaan baik nasional maupun internasional seperti <b style="color: black;"> <i>Indonesia ICT Award (INAICTA)</i></b>, <b style="color: black;"> <i>International Robot Olympiad (IRO)</i></b>, <b style="color: black;"> <i>Kontes Robot Cerdas Indonesia (KRCI)</i></b>, <b style="color: black;"> <i>International Maze Robot Competition</i></b>, dan <b style="color: black;"> <i>Program Kreatifitas Mahasiswa-DIKTI</i></b>.
							Kedua pemuda Batak ini memiliki ide untuk mengolaborasikan budaya dan teknologi. Mereka ingin menepis anggapan bahwa hal yang berhubungan dengan budaya itu kuno dan membosankan. Keduanya sepakat bahwa teknologi dan budaya dapat diintegrasikan. Ide tersebut kemudian direalisasikan dengan menciptakan sebuah robot cerdas yang dinamakan Robot Sigale-gale. Robot Sigale-gale ini dapat menari dan gerakannya mengikuti tempo musik yang dimainkan. Dari segi elektrik robot ini menggunakan mikrocontroller AT Mega 256, Akuator: 18 digital servo AX-12, dan untuk sensor menggunakan satu unit sensor suara dan satu unit gyroscope. Dari segi mekanik, robot ini menggunakan 80% acrylic dan 20% resin, namun beberapa bagian juga menggunakan gips. Robot yang berbobot 2050-gram dan tinggi 40-centimeter ini telah dipertunjukkan dalam beberapa kontes dan mendapat berbagai penghargaan antara lain memenangkan International Robot Olympiad 13thpada tahun 2011 dengan menyabet Technical Award. <br> Rabu, 2 September 2014, keduanya, yang diwakili oleh Chrisman Silaban, menghibahkan Robot Sigale-gale ini kepada TB Silalahi Center untuk menjadi koleksi Museum. Hal ini dilatarbelakangi keinginan keduanya untuk menginspirasi banyak orang melalui hasil karya mereka. Di samping itu, kesibukan keduanya juga membuat waktu luang yang sangat minim untuk memelihara robot ini. Denny Sihombing telah menjadi tenaga profesional di salah satu perusahan tambang di Sulawesi, sedangkan Krisman telah berkesempatan mendapatkan beasiswa dari Lembaga Pengelola Dana Pendidikan (LPDP) untuk melanjutkan pendidikan ke jenjang S2 di University of Birmingham, Inggris (2015-2016) dalam program studi M.Sc in Embedded System.
							“Kalau disimpan di laboratorium komputer UI, yang melihat hanya anak-anak TRUI. Sedangkan kalau bisa dipamerkan di museum kan bisa dinikmati oleh banyak orang Batak khususnya generasi muda,” ujar Crisman.
							Direktur Museum TB Silalahi Center, Duma Yanti Silalahi, menyambut baik akan hal ini dan mengaku salut kepada keduanya yang berkutat dengan dunia teknologi tetapi tidak meninggalkan budaya dan kearifan lokal suku Batak. Beliau berpendapat bahwa pemikiran kedua pemuda ini sejalan dengan tujuan pendirian Museum TB Silalahi Center (baik Museum Batak ataupun Museum Jejak langkah dan Sejarah TB Silalahi) untuk menginspirasi dan memotivasi generasi muda memperoleh hidup yang lebih baik dengan belajar dari sejarah nenek moyang dan pengalaman para pendahulunya.
							“Robot Sigale-gale ini nantinya akan dipamerkan di Museum Jejak Langkah dan Sejarah TB Silalahi di bagian yang membahas tentang Yayasan Soposurung. Mereka ini adalah alumni SMA Negeri 2 Yayasan Soposurung dan segmentasi museum ini adalah generasi muda, khususnya pelajar, sehingga sangat tepat dipamerkan di sana. Jadi setelah melihat robot ini, diharapkan akan banyak generasi muda terinspirasi dan termotivasi untuk membuat karya sendiri atau bahkan mengembangkan ide ini,” terangnya.
